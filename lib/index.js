function countWords(sentenceEnglish, sentencePortuguese) {
	var english = _.chain(sentenceEnglish.toUpperCase())
						.words()
						.filter(function(word) {
							return !_.includes(stopWordsEnglish, word);
						})
						.countBy(_.identity)
						.map(function(size, word) {
							return { text: word, size: size};
						})
						.value();

	var portuguese = _.chain(sentencePortuguese.toUpperCase())
					.words()
					.filter(function(word) {
						return !_.includes(stopWordsPortuguese, word);
					})
					.countBy(_.identity)
					.map(function(size, word) {
						return { text: word, size: size};
					})
					.value();

	return english.concat(portuguese);
}

